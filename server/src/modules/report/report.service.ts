export type Lang = 'vi' | 'en';
export type ReportType = 'tuition' | 'scores' | 'students' | 'classes';

export interface ReportColumn {
  header: string;
  key: string;
  width?: number;
}
export interface ReportTable {
  title: string;
  subtitle: string;
  columns: ReportColumn[];
  rows: Record<string, string | number>[];
}

export interface ReportQuery {
  classId?: string;
  status?: string;
  from?: string;
  to?: string;
}

